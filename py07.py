# Mapping data type -> A mapping type stores data as key-value pairs (like a dictionary).
# Dictionary (dict) - The Only Mapping Type in Python

# Dictionary uses curly braces {} with key:value pairs
student = {
    "name": "John",
    "age": 20,
    "grade": "A"
}

print(student)  # {'name': 'John', 'age': 20, 'grade': 'A'}