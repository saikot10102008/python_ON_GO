 # Data types -> type of stored data for instance string which is group of characters

# Python numeric data types

# int - whole numbers (integers) like 100, 200, -100
# float - decimal numbers like 1.145, -10.55
# complex - numbers with real and imaginary parts
# Format: a + bj (where j is the imaginary unit)
# Example: 3 + 4j

# Variable assignments with different numeric types
a = 10        # int
b = 10.20     # float
c = 3 + 4j    # complex

# Printing the values
print(a, b, c)

# Printing the data types
print(type(a), type(b), type(c))




# int = integer numbers (no decimal point)
#
# float = floating-point numbers (with decimal point)
#
# complex = complex numbers (real + imaginary parts)
#
# type() function shows the data type of a variable
