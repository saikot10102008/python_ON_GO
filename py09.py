# PEMDAS stands for:
# P - Parentheses ()
# E - Exponents **
# M - Multiplication *
# D - Division / or //
# A - Addition +
# S - Subtraction -

# Example showing PEMDAS:
result = 10 + 5 * 2  # Multiplication first, then addition
print(f"10 + 5 * 2 = {result}")  # 10 + 10 = 20

result = (10 + 5) * 2  # Parentheses first
print(f"(10 + 5) * 2 = {result}")  # 15 * 2 = 30

result = 2 ** 3 + 4 * 5  # Exponents first, then multiplication, then addition
print(f"2 ** 3 + 4 * 5 = {result}")  # 8 + 20 = 28