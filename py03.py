# Boolean data type in Python
# Represents True or False values (logical values)
# Used for logical operations and conditions

# Boolean variable assignments
is_raining = True
is_sunny = False

# Printing the boolean values
print(is_raining, is_sunny)


"""
True and False are the two Boolean values in Python (note: they must be capitalized)

Boolean type is called bool in Python

Booleans are used for:

Logical operations (AND, OR, NOT)

Conditional statements (if/else)

Comparisons (==, !=, >, <, etc.)

type(is_raining) would show: <class 'bool'>
"""