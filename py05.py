# Sequence type

# Sequence types store multiple items in order. Here are the main ones: string , list , tuple

# Strings are for text, can use single or double quotes , cannot be changed , group of characters
text = "this is a string"  # or text = 'this is a string'
print(text)
print(type(text))  # Shows: <class 'str'>

# List uses square brackets []
# Can store multiple items, can be changed ,can store multiple types of data
my_list = ['data1', 'data2', 'data3']
print(my_list)  # Shows: ['data1', 'data2', 'data3']
print(type(my_list))

# Tuple uses parentheses ()
# Can store multiple items, but CANNOT be changed
my_tuple = ('data1', 'data2', 'data3')
print(my_tuple)  # Shows: ('data1', 'data2', 'data3')
print(type(my_tuple))



