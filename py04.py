# None is a special constant in Python
# It represents the absence of a value or a null value
# It is often used as a placeholder or to indicate "nothing" or "empty"

# Assigning None to a variable
result = None  # This means result doesn't have any value, it's empty/null

# Printing the value of result
print(result)  # This will output: None

# Note: None is not the same as:
# - 0 (which is a numeric value)
# - False (which is a boolean value)
# - "" (which is an empty string)
# - [] (which is an empty list)
